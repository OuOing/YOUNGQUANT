// jwtSecretKey loads JWT secret from environment, falls back to dev default
func loadJWTSecret() []byte {
	if s := os.Getenv("JWT_SECRET"); s != "" {
		return []byte(s)
	}
	return []byte("youngquant-dev-secret-change-in-production")
}

